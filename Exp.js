// 常用正则表达式
export default {
  // 整型
  int:/^\d+$/,
  // 帐号
  account:/^\w{6,16}$/,
  // 密码
  password:/^(?!^\d+$)(?!^[a-zA-Z]+$)(?!^[\~\`\!\@\#\$\%\^\&\*\(\)\-\+\=\{\[\}\]\|\\\:\;\”\’\<\,\>\.\?\/]+$)[\w\~\`\!\@\#\$\%\^\&\*\(\)\-\+\=\{\[\}\]\|\\\:\;\”\’\<\,\>\.\?\/]{8,16}$/,
  // 旧密码
  simplePassword:/^[\w\~\`\!\@\#\$\%\^\&\*\(\)\-\+\=\{\[\}\]\|\\\:\;\”\’\<\,\>\.\?\/]{6,16}$/,
  // 邮箱
  email:/^((?=[a-zA-Z0-9])[a-zA-Z0-9._-]+)?[a-zA-Z0-9]{1}@[a-zA-Z0-9]+(-?[a-zA-Z0-9]+)?(\.[a-zA-Z0-9]+)*(\.[a-zA-Z]{2,}){1}$/,
  // 郵箱或用戶名
  account_email:/^(\w{6,16}|(((?=[a-zA-Z0-9])[a-zA-Z0-9._-]+)?[a-zA-Z0-9]{1}@[a-zA-Z0-9]+(-?[a-zA-Z0-9]+)?(\.[a-zA-Z0-9]+)*(\.[a-zA-Z]{2,}){1}))$/,
  // 比特幣地址
  bitAddress:/^[a-zA-Z0-9]{16,100}$/,
  // 比特幣格式
  bitcoin:/^[0-9\.]+$/,
  // 域名
  domainName:/^[a-zA-Z0-9]+(-?[a-zA-Z0-9]+)?(\.[a-zA-Z]{2,}){1,2}$/
}